
public class ACD_JavaS1A4 {
	public static void main (String args[]) {
		// This is a program to show the expression result
		int numX = 5 ;
		int numY = 10 ;
		System.out.println("the value for the expression : numX + numY * 2 =" + (numX + numY * 2) ); //25
		System.out.println("the value for the expression : numX - numY + 2 =" + (numX - numY + 2) ); //-3
		System.out.println("the value for the expression :(numX +numY ) * 2 =" + ((numX +numY ) * 2) );//30
		System.out.println("the value for the expression : numY % numX =" + (numY % numX) );//0
	}
}
